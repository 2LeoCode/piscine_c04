#include <unistd.h>

void	ft_putnbr(int nb)
{
	int i;

	i = nb;
	if (nb < 0)
	{
		write(1, '-', 1);
		if (i == -2147483648)
			i = 2147483647;
		else
			i *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		write(1, i + 48, 1);
}
